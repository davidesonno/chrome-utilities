#!/usr/bin/env node

import fs from 'node:fs/promises';
import path from 'node:path';
import vm from 'node:vm';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, '..');
const snippetsDir = path.join(repoRoot, 'snippets');
const generatedDir = path.join(repoRoot, 'generated');
const manifestPath = path.join(repoRoot, 'manifest.json');
const generatedIndexPath = path.join(generatedDir, 'generated-index.js');
const generatedLoaderPath = path.join(generatedDir, 'generated-loader.js');

const writeFiles = !process.argv.includes('--check');

function createSandbox(filePath, collected) {
  const noop = () => {};
  const sandboxChrome = {
    commands: { onCommand: { addListener: noop, removeListener: noop } },
    tabs: { query: noop, create: noop, group: noop },
    storage: { sync: { get: noop, set: noop }, onChanged: { addListener: noop } },
    runtime: { onInstalled: { addListener: noop }, getURL: (value) => value },
  };

  return vm.createContext({
    console,
    chrome: sandboxChrome,
    self: {},
    registerSnippet(meta) {
      collected.push({ filePath, meta });
    },
    importScripts: noop,
    setTimeout,
    clearTimeout,
    Promise,
    Object,
    Array,
    Map,
    Set,
    Date,
    JSON,
    Math,
    Error,
  });
}

async function loadSnippetMetadata(filePath) {
  const source = await fs.readFile(filePath, 'utf8');
  const collected = [];
  const sandbox = createSandbox(filePath, collected);
  vm.runInContext(source, sandbox, { filename: filePath });

  if (collected.length !== 1) {
    throw new Error(`${path.relative(repoRoot, filePath)} must call registerSnippet exactly once.`);
  }

  const meta = collected[0].meta;
  if (!meta || !meta.id || !meta.name || !meta.description) {
    throw new Error(`${path.relative(repoRoot, filePath)} is missing required snippet metadata.`);
  }

  return { file: path.basename(filePath), meta };
}

function normalizeCommand(command, fileName, snippetId) {
  if (!command || typeof command !== 'object') {
    throw new Error(`${fileName} has an invalid command entry.`);
  }

  if (!command.id) {
    throw new Error(`${fileName} command definitions must include an id.`);
  }

  const suggestedKey = command.suggested_key || {};
  const hasPlatformKey = ['default', 'windows', 'mac', 'linux', 'chromeos'].some((platform) => suggestedKey[platform]);
  if (!hasPlatformKey) {
    throw new Error(`${fileName} command ${command.id} is missing a platform-specific suggested key.`);
  }

  return {
    id: command.id,
    description: command.description,
    suggested_key: suggestedKey,
    snippetId,
  };
}

async function main() {
  const entries = await fs.readdir(snippetsDir, { withFileTypes: true });
  const snippetFiles = entries
    .filter((entry) =>
      entry.isFile() &&
      entry.name.endsWith('.js') &&
      !entry.name.startsWith('generated-')
    )
    .map((entry) => path.join(snippetsDir, entry.name))
    .sort((a, b) => path.basename(a).localeCompare(path.basename(b)));

  const snippets = [];
  const manifestCommands = {};
  const seenIds = new Set();
  const seenCommands = new Set();

  for (const filePath of snippetFiles) {
    const { file, meta } = await loadSnippetMetadata(filePath);

    if (seenIds.has(meta.id)) {
      throw new Error(`Duplicate snippet id detected: ${meta.id}`);
    }
    seenIds.add(meta.id);

    if (!meta.command) {
      throw new Error(`${path.relative(repoRoot, filePath)} must provide exactly one command.`);
    }

    const normalizedCommand = normalizeCommand(meta.command, file, meta.id);

    if (seenCommands.has(normalizedCommand.id)) {
      throw new Error(`Duplicate command id detected: ${normalizedCommand.id}`);
    }
    seenCommands.add(normalizedCommand.id);
    manifestCommands[normalizedCommand.id] = {
      suggested_key: normalizedCommand.suggested_key,
      description: meta.description,
    };

    snippets.push({
      id: meta.id,
      name: meta.name,
      description: meta.description,
      file,
      command: {
        id: normalizedCommand.id,
        suggested_key: normalizedCommand.suggested_key,
      },
    });
  }

  const manifest = JSON.parse(await fs.readFile(manifestPath, 'utf8'));
  manifest.commands = manifestCommands;

  const generatedLoader = [
    '// Generated by tools/build-snippets.mjs. Do not edit by hand.',
    ...snippetFiles.map((filePath) => `importScripts('${path.posix.join('..', 'snippets', path.basename(filePath))}');`),
    '',
  ].join('\n');

  const generatedIndex = [
    '// Generated by tools/build-snippets.mjs. Do not edit by hand.',
    `window.__SNIPPETS__ = ${JSON.stringify(snippets, null, 2)};`,
    '',
  ].join('\n');

  const manifestJson = JSON.stringify(manifest, null, 2) + '\n';

  if (!writeFiles) {
    console.log(`Validated ${snippets.length} snippets from ${path.relative(repoRoot, snippetsDir)}.`);
    return;
  }

  await fs.mkdir(generatedDir, { recursive: true });
  await fs.writeFile(manifestPath, manifestJson, 'utf8');
  await fs.writeFile(generatedIndexPath, generatedIndex, 'utf8');
  await fs.writeFile(generatedLoaderPath, generatedLoader, 'utf8');

  console.log(`Generated manifest and snippet index for ${snippets.length} snippets.`);
}

main().catch((error) => {
  console.error(error.message || error);
  process.exitCode = 1;
});